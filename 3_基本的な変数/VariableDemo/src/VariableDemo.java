public class VariableDemo {
    public static void main(String[] args) {
        int a = 0;
        float b = 1.11f;
        double c = 1.11;
        char d = 'こ';
        String e = "こんにちは";
        boolean f = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        a = 1;
        b = 2.22f;
        c = 2.22;
        d = 'に';
        e = "こんばんは";
        f = false;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}