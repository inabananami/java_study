public class IfElse {
    public static void main(String[] args) {
        int a = 1, b = 2;
        if(a == 0) {
            System.out.println(a);
        }
        else if(b == 1) {
            System.out.println(b);
        }
        else {
            System.out.println("error");
        }
    }
}
