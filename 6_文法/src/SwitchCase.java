public class SwitchCase {
    public static void main(String[] args) {
        int a = 1, b = 1;
        switch(a) {
            default:
                System.out.println(a);
                break;
            case 1:
                System.out.println("2");
                break;
            case 2:
                System.out.println(b);
                break;
        }
    }
}
